#pragma once

// ================================================
// 時間管理
//  フレームの経過時間（デルタタイム）を提供する。
//  ゲーム内の移動やアニメーションは、フレーム数ではなく
//  経過時間を基準にすることで、処理速度に依存しなくなる。
//
//  使い方:
//    float dt = Time::GetDeltaTime();          // 前フレームからの秒数
//    position.x += speed * dt;                 // 1秒あたり speed ピクセル移動
// ================================================
namespace Time
{
	// --- システム（触らなくてよい） ---
	void Initialize();
	void Update();
	float NowSeconds();       // 現在の経過秒（Update を介さない即時値。メインループのフレーム制御用）

	// --- ゲームコードで使う ---
	float GetDeltaTime();     // 前フレームからの経過時間（秒）
	float GetElapsedTime();   // ゲーム開始からの経過時間（秒）
}
