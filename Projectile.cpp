#include "Projectile.h"

//======================================================================================//
//  発射物が消える範囲（この外に出たら自分で回収する）								//
//======================================================================================//
static const float KILL_RIGHT = 1200.0f;	// 右へ飛びすぎたら消す
static const float KILL_LEFT = -1200.0f;	// 左へ行きすぎたら消す（敵弾用の保険）
static const float KILL_TOP = 1400.0f;	// 上へ行きすぎたら消す
static const float KILL_BOTTOM = -1400.0f;	// 下へ行きすぎたら消す

//======================================================================================//
//  Update：毎フレーム、自分の速度で飛ぶ＋画面外なら自分で消える						//
//--------------------------------------------------------------------------------------//
// エンジンが自動で呼ぶ。ここでは「移動」と「画面外の回収」だけ。					//
// 敵との当たり判定は GameScene 側のループが担当する（今はそのまま）。				//
//======================================================================================//
void Projectile::Update(float dt)
{
	// 1) 自分の速度で移動
	Float3 p = GetPosition();
	p.x += velocityX * dt;
	p.y += velocityY * dt;
	SetPosition(p);

	// 2) 画面外なら自分で消える（無効化 → フレーム終わりのAdjustObjectListで安全に削除）
	if (p.x > KILL_RIGHT || p.x < KILL_LEFT ||
		p.y > KILL_TOP || p.y < KILL_BOTTOM)
	{
		Hide();				// 描画無効化
		Deactivation();		// 無効化（GameScene 側もこれを見てリストから外す）
		return;				// 消えるので以降は不要
	}

	// 3) 基底 Update：当たり判定の同期＋アニメーション更新
	GameObject::Update(dt);
}